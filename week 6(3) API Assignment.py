# Q-2 Give advantages and disadvantages of using API.

# Advantages API :
#     
# 1. Increased Interoperability : API solutions enable different software system to communicate with each other, regardless of        their programming language or platform.
# 
# 2. Improved Efficiency : API driven infrastructures enable developers to reuse  code and functionality from existing software
#    system, saving time and reducing the amount of code that needs to be written from scatch.
#    
# 3. Increased flexibility : APIs provide developers with greater flexibility,
#    enabling them to choose the best programming languages and frameworks for thier needs.
#    
# Disadvantage API :
# 
# 1. Security concerns : API infrastructures can be vulnerable to security breaches.
# 
# 2. complexity : APIs can be complex, requiring a significant amount of development version effort to implement and maintain.
# 
# 3. compatibility Issues : APIs can have compatibility issues between defferent versions of the same software system or between different system, which can lead to errors and system failures.

